public class Rectangle {
    // Declaring attributes (member variables)
    double length;
    double width;

    // Creating a no-argument constructor
    public Rectangle () {
        this.length = 1.0;
        this.width = 1.0;
    }

    // Creating a parameterized constructor
    public Rectangle(double length, double width) {
        this.length = length;
        this.width = width;
    }
}
